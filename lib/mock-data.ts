// Records
type RecordSeed = Omit<Record, 'createdAt' | 'updatedAt' | 'comments' | 'actionHistory'> & {
  createdAt?: Date;
  updatedAt?: Date;
};

const baseRecord = (seed: RecordSeed): Record => {
  const createdAt = seed.createdAt || seed.dateReceived;
  return {
    ...seed,
    createdAt,
    updatedAt: seed.updatedAt || seed.dateReceived,
    comments: [
      {
        id: `${seed.id}-comment-1`,
        author: seed.createdBy,
        text: `Logged with ${seed.mainCategory} / ${seed.discipline} tags and routed to ${seed.responsibleParty || 'the project team'}.`,
        createdAt,
      },
    ],
    actionHistory: [
      {
        id: `${seed.id}-hist-1`,
        action: 'Record logged and classified',
        performedBy: seed.createdBy,
        timestamp: createdAt,
        newValue: seed.status,
      },
      ...(seed.actionStatus
        ? [{
            id: `${seed.id}-hist-2`,
            action: 'Action status updated',
            performedBy: seed.responsibleParty || seed.createdBy,
            timestamp: seed.updatedAt || seed.dateReceived,
            newValue: seed.actionStatus,
          }]
        : []),
    ],
  };
};

const attachment = (recordId: string, name: string, uploadedAt: Date, uploadedBy: string) => ({
  id: `${recordId}-att-${name.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`,
  name,
  size: 420000,
  type: name.endsWith('.pdf') ? 'application/pdf' : name.endsWith('.dwg') ? 'application/dwg' : 'image/jpeg',
  uploadedAt,
  uploadedBy,
});

