// Required Actions
export function generateMockActions(records: Record[]): RequiredAction[] {
  const impactType = (record: Record): RequiredAction['impactType'] => {
    if (record.hasClaimRisk) return 'Claim';
    if (record.hasCostImpact) return 'Cost';
    if (record.hasProgrammeImpact) return 'Programme';
    if (record.mainCategory === 'Quality/NCR') return 'Quality';
    return 'Programme';
  };

  return records
    .filter((record) => record.actionStatus)
    .map((record, index) => ({
      id: `act-${String(index + 1).padStart(4, '0')}`,
      title: record.requiredAction || `Review ${record.reference}`,
      recordId: record.id,
      projectId: record.projectId,
      responsiblePerson: record.responsibleParty || 'Project Team',
      dueDate: record.actionDueDate || record.responseDueDate || record.dateReceived,
      priority: record.actionStatus === 'Closed' ? 'Low' : record.priority,
      status: record.actionStatus as RequiredAction['status'],
      impactType: impactType(record),
      nextStep: record.nextStep || 'Review linked record and update status.',
      linkedRecord: record,
    }));
}

// Programme Impacts
export function generateMockProgrammeImpacts(records: Record[]): ProgrammeImpact[] {
  const recordsWithImpact = records.filter((r) => r.hasProgrammeImpact);

  return recordsWithImpact.map((record, index) => {
    const linkedIds = [
      ...record.linkedDelays,
      ...record.linkedInstructions,
      ...record.linkedApprovals,
      ...record.linkedSiteIssues,
    ].filter((id) => id !== record.id);
    const linkedRecords = [record, ...records.filter((item) => linkedIds.includes(item.id))];

    return {
      id: `prog-${String(index + 1).padStart(4, '0')}`,
      recordId: record.id,
      projectId: record.projectId,
      delayReason: record.programmeImpactDesc || 'Programme impact under review',
      eventDate: record.eventDate || new Date(),
      programmeUpdateRef: `PROG-2026-${String(index + 1).padStart(3, '0')}`,
      responsibleParty: record.responsibleParty || 'To be assigned',
      potentialTimeImpact: record.projectId === 'p1' ? '7 days' : '3-5 days',
      responseDueDate: record.responseDueDate || new Date(),
      status: record.status === 'Closed' ? 'Resolved' : record.status === 'In Review' ? 'Under Review' : 'Identified',
      linkedRecords,
    };
  });
}

